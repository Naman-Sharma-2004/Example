// <------------------------ Typecasting in C ----------------------------->

#include <stdio.h>
#include <conio.h>

// Typecasting Syntax
// (Type) value;

// Typecasting is used to convert one data type into another.
int main()
{
    int a = 3;
    float b = 54;
    printf("The value of a is %d\n", (int)b);

    return 0;
}
